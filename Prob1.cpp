#include <iostream>
using namespace std;

int main() {

    cout << 1 << endl;
    cout << 10 << endl;
    cout << 100 << endl;
    cout << 1000 << endl;
    cout << 10000 << endl;
    cout << 100000 << endl;
    cout << 1000000 << endl;
    cout << 10000000 << endl;
    cout << 100000000 << endl;
    cout << 1000000000 << endl;
    cout << 10000000000 << endl;
    cout << 100000000000 << endl;
    cout << 1000000000000 << endl;

    cout << 1.0 << endl;
    cout << 10.0 << endl;
    cout << 100.0 << endl;
    cout << 1000.0 << endl;
    cout << 10000.0 << endl;
    cout << 100000.0 << endl;
    cout << 1000000.0 << endl;
    cout << 10000000.0 << endl;
    cout << 100000000.0 << endl;
    cout << 1000000000.0 << endl;
    cout << 10000000000.0 << endl;
    cout << 100000000000.0 << endl;
    cout << 1000000000000.0 << endl;

    // The difference is that when printed as a floating point, the numbers come out in scientific notation.

    return 0;
}
